import 'dart:math';

void main() {
  print('sin(45)=${sin(45) * (pi / 180)}');
  print('cos(0)=${cos(0 * (pi) / 180)}');
  print('tan(45)=${tan(45 * (pi / 180))}');
  // inverse function 
  print('asin(0.5)=${asin(0.5)*(180/pi)}');
  print('acos(1)=${acos(1)*(180/pi)}');
  print('atan(1)=${atan(1)*(180/pi)}');
  

}
